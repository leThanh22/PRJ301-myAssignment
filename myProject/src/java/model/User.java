package model;

/**
 *
 * @author Admin
 */
public class User {
    private int id;
    private String name;
    private String pass;
    private String email;
    private int role;

    public User() {
    }

    public User(int id, String name, String pass, String email, int role) {
        this.id = id;
        this.name = name;
        this.pass = pass;
        this.email = email;
        this.role = role;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

    
}
